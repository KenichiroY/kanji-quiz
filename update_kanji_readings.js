/**
 * カタカナをひらがなに変換
 */
function katakanaToHiragana(str) {
  return str.replace(/[\u30A1-\u30F6]/g, match => {
    return String.fromCharCode(match.charCodeAt(0) - 0x60);
  });
}

/**
 * 読み方文字列をパースして個別の読み方配列に分解
 */
function parseReadings(readingStr, isOnyomi) {
  if (!readingStr || readingStr === '–' || readingStr === '-') {
    return [];
  }

  // 「・」で分割
  const parts = readingStr.split('・');
  const readings = [];

  for (const part of parts) {
    let reading = part.trim();
    if (!reading) continue;

    // 音読みの場合、カタカナをひらがなに変換
    if (isOnyomi) {
      reading = katakanaToHiragana(reading);
    }

    // 送り仮名を抽出 (括弧内)
    let okurigana = null;
    const match = reading.match(/^(.+?)（(.+?)）$/);
    if (match) {
      reading = match[1];
      okurigana = match[2];
    }

    readings.push({ reading, okurigana });
  }

  return readings;
}

